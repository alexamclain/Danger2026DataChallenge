#!/usr/bin/env python3
"""Structured-basis audit for centered Cauchy-Binet noncancellation.

The centered marginal determinant has the exact form

    det(A B R^t)

where A and R are component-difference coefficient matrices and B is the
Hermitian trace form.  A tempting p-adic proof would find a natural integral
basis/filtration in which the exterior Cauchy-Binet expansion has a surviving
unit initial term and all other terms are forced into the maximal ideal.

This audit tests that idea in small actual-CM rows.  It deliberately restricts
to structured bases:

* the packet power basis;
* the first available Frobenius normal basis generated by a small power of X;
* the middle-Frobenius +/- eigenspace basis when the packet degree is even;
* a Hermitian Gram-Schmidt basis starting from the power basis.

These are not proof objects for p24, but they are the finite-field reductions
one would expect to expose a simple p-integral triangularization if it existed.
"""

